a = 21
b = 10

print("Value of a is ", a)
print("Value of b is ", b, "\n")

## Addition
c = a + b
print("Value of ", a," + ", b," is ", c, "\n")

## Substraction
c = a - b
print("Value of ", a," - ", b," is ", c, "\n")

## Multiplication
c = a * b
print("Value of ", a," * ", b," is ", c, "\n")

## Division
c = float(a) / float(b)
print("Value of ", a," / ", b," is ", c, "\n")

## Modulus
c = a % b
print("Value of ", a," % ", b," is ", c, "\n")

## Square
a = 2
b = 3
c = a ** b
print("Value of ", a," ** ", b," is ", c, "\n")

## Floor division
a = 10
b = 3
c = a // b
print("Value of ", a," // ", b," is ", c, "\n")

## Negation
a = 21
b = 10

print("Negation of", a , "is ", -a)
print("Negation of", b , "is ", -b)
