my_dict = {'Name': 'Zara', 'Age': 7, 'Class': 'First'}

print(f"my_dict: {my_dict}")

## Updating dictionary
my_dict['Age'] = 8

## Add or Append new key and value
my_dict['School'] = "ITS Surabaya"

print(f"my_dict: {my_dict} \n")

## Deleting dictionary by keys
del my_dict['School']

print(f"my_dict: {my_dict} \n")

del my_dict

print(f"my_dict: {my_dict} \n")